function InsertionSort(arr) {
    for (let i = 0; i < arr.length; i++) {
        let key = arr[i];
        let j;
        for (j = i - 1; j >= 0 && arr[j] > key; j--) {
            arr[j + 1] = arr[j];
        }
        arr[j + 1] = key;
    }
    return arr;
}



// Example usage:
let insertionSortedArray =InsertionSort([64, 34, 25, 12, 22, 11, 90]);
console.log("Insertion Sort:", insertionSortedArray);
